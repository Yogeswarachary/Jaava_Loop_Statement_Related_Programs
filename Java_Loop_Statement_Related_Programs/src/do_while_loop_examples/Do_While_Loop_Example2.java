package do_while_loop_examples;

public class Do_While_Loop_Example2 {
	public static void main(String[] args) {
		int i=1;
		//do-while loop
		do {
			//Body of do-while loop
			System.out.println("Hello! Yogesh");
			//Update expression
			i++;
		}
		//Test expression
		while(i<5);
	}
}
